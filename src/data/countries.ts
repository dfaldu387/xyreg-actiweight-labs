/**
 * Group countries by their first letter for efficient navigation
 */
export const groupedCountries = countries.reduce<Record<string, string[]>>((groups, country) => {
  const firstLetter = country.charAt(0).toUpperCase();
  if (!groups[firstLetter]) {
    groups[firstLetter] = [];
  }
  groups[firstLetter].push(country);
  return groups;
}, {});

/**
 * Find the first country that starts with the given letter
 * @param letter The letter to search for
 * @returns The index of the first country starting with the letter, or -1 if none found
 */
export const findCountryIndexByFirstLetter = (letter: string): number => {
  const upperLetter = letter.toUpperCase();
  return countries.findIndex(country => 
    country.charAt(0).toUpperCase() === upperLetter
  );
};
